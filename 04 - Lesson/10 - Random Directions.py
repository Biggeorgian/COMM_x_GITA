# рЃерЃћрЃЏрЃЮрЃЋрЃўрЃбрЃљрЃюрЃЮрЃЌ random рЃЉрЃўрЃЉрЃџрЃўрЃЮрЃЌрЃћрЃЎрЃљ рЃЊрЃљ рЃЊрЃљрЃЋрЃљрЃњрЃћрЃюрЃћрЃарЃўрЃарЃЮрЃЌ рЃерЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃўрЃЌрЃў рЃЉрЃўрЃџрЃўрЃЎрЃў
import random

random_direqctions = [random.choice(["l", "r"]) for _ in range(5)]
# рЃфрЃЋрЃџрЃљрЃЊрЃћрЃЉрЃў рЃўрЃбрЃћрЃарЃљрЃфрЃўрЃўрЃАрЃљ рЃЊрЃљ рЃЦрЃБрЃџрЃћрЃЉрЃўрЃАрЃЌрЃЋрЃўрЃА
user_input = ""
steps = 0
score = 0

print(f"рЃљрЃ«рЃџрЃљ рЃЏрЃЮрЃЊрЃўрЃЌ рЃЋрЃўрЃЌрЃљрЃЏрЃљрЃерЃЮрЃЌ. рЃЌрЃБ рЃЊрЃарЃЮрЃќрЃћ рЃљрЃЊрЃарЃћ рЃЏрЃЮрЃњрЃЉрЃћрЃќрЃарЃЊрЃљрЃЌ, рЃБрЃЉрЃарЃљрЃџрЃЮрЃЊ рЃљрЃЎрЃарЃўрЃцрЃћрЃЌ exit.")
print(f"рЃгрЃљрЃарЃЏрЃЮрЃўрЃЊрЃњрЃўрЃюрЃћрЃЌ, рЃарЃЮрЃЏ рЃњрЃќрЃљрЃќрЃћ рЃЏрЃўрЃЊрЃўрЃ«рЃљрЃарЃЌ рЃЊрЃљ рЃБрЃфрЃЉрЃљрЃЊ рЃњрЃќрЃљрЃ»рЃЋрЃљрЃарЃћрЃЊрЃўрЃюрЃќрЃћ рЃљрЃдрЃЏрЃЮрЃЕрЃюрЃЊрЃўрЃЌ.")

while steps < 5:
    steps += 1
    user_input = input("рЃАрЃљрЃўрЃЌ рЃњрЃљрЃБрЃ«рЃЋрЃћрЃЋрЃЌ рЃЏрЃљрЃарЃфрЃ«рЃюрЃўрЃЋ: l рЃЌрЃБ рЃЏрЃљрЃарЃ»рЃЋрЃюрЃўрЃЋ: r ? ")
    if user_input.lower() == "l" or user_input.lower() == "r":
        if user_input == random_direqctions[steps - 1]:
            score += 1
            print("рЃЎрЃљрЃарЃњрЃўрЃљ, рЃЎрЃљрЃарЃњрЃўрЃљ, рЃЎрЃљрЃарЃњрЃўрЃљрЃљрЃљрЃљрЃљ...")
            print("\nрЃгрЃўрЃю рЃЎрЃўрЃЊрЃћрЃЋ рЃћрЃарЃЌрЃў рЃњрЃќрЃљрЃњрЃљрЃАрЃљрЃДрЃљрЃарЃўрЃљ")
        else:
            print("рЃцрЃарЃЌрЃ«рЃўрЃџрЃљрЃЊ, рЃцрЃћрЃ«рЃў рЃљрЃарЃљрЃцрЃћрЃарЃерЃў рЃЕрЃљрЃљрЃЊрЃњрЃљ...")
            print("\nрЃгрЃўрЃю рЃЎрЃўрЃЊрЃћрЃЋ рЃћрЃарЃЌрЃў рЃњрЃќрЃљрЃњрЃљрЃАрЃљрЃДрЃљрЃарЃўрЃљ")
    elif user_input.lower() == "exit":
        print("рЃљрЃЏ рЃЏрЃЮрЃАрЃљрЃ«рЃЋрЃћрЃЋрЃерЃў рЃњрЃћрЃЏрЃерЃЋрЃўрЃЊрЃЮрЃЉрЃћрЃЉрЃўрЃЌ ­ЪЦ║")
        break
    else:
        print("рЃљрЃЏ рЃЌрЃљрЃЏрЃљрЃерЃерЃў рЃЊрЃљрЃАрЃљрЃерЃЋрЃћрЃЉрЃўрЃљ рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ l рЃљрЃю r рЃерЃћрЃДрЃЋрЃљрЃюрЃљ.\nрЃЌрЃБ рЃЌрЃљрЃЏрЃљрЃерЃў рЃЏрЃЮрЃњрЃЉрЃћрЃќрЃарЃЊрЃљрЃЌ, рЃБрЃЉрЃарЃљрЃџрЃЮрЃЊ рЃљрЃЎрЃарЃўрЃцрЃћрЃЌ exit.")

if score == 5:
    print("\nрЃДрЃЮрЃЕрЃљрЃд! рЃфрЃ«рЃЮрЃЋрЃарЃћрЃЉрЃўрЃА рЃњрЃќрЃљрЃќрЃћ рЃерЃћрЃю рЃАрЃгрЃЮрЃарЃћрЃЊ рЃўрЃљрЃарЃћ ­Ъўѓ­ЪјЅ")
elif user_input.lower() == "exit":
    print("рЃЎрЃљрЃарЃњрЃљрЃЊ...")
else:
    print("\nрЃАрЃљрЃЏрЃгрЃБрЃ«рЃљрЃарЃЮрЃЊ рЃфрЃ«рЃЮрЃЋрЃарЃћрЃЉрЃўрЃА рЃџрЃљрЃЉрЃўрЃарЃўрЃюрЃЌрЃерЃў рЃЊрЃљрЃўрЃЎрЃљрЃарЃњрЃћ ­ЪЦ║")
    print("рЃЏрЃљрЃњрЃарЃљрЃЏ, рЃ»рЃћрЃа рЃЎрЃўрЃЊрЃћрЃЋ рЃњрЃљрЃЦрЃЋрЃА рЃерЃљрЃюрЃАрЃў, рЃАрЃфрЃљрЃЊрЃЮ рЃ«рЃћрЃџрЃљрЃ«рЃџрЃљ ­ЪЌ║")
